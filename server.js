const http = require('http');//חיבור השרת לספריית HTTP
const app = require('./app');//חיבור השרת לאפליקציה שיצרנו בקובץ APP.JS
const port = 4000;//הגדרת הפורט שעליו השרת יאזין
const srv = http.createServer(app);//יצירת אובייקט שרת שאותה תנהל אפליקצית APP במקום לכתוב לבד את הקוד של השרת
srv.listen(port, () => {//אובייקט השרת יאזין לפורט שהוגדר מה שאומר שיש לנו שרת שפועל כעת 
    console.log('Server on air');//הודעה לקונסול שהשרת פועל באמצעות פונקציית חזרה כפרמטר בפונקציית ליסן
});
